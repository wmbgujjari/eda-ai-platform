SELECT
    om2.ID AS SECTION_ID,
    NAA.CUSTOMER_ACCOUNT_NO,
    TO_CHAR(TO_DATE(TO_CHAR(NAA.ENE_DATE), 'YYYYMMDD'), 'YYYY-MM-DD HH24:MI:SS') AS DAYS,
    WRD.TEMPERATURE,
    WRD.HUMIDITY,
    WRD.WIND_SPEED,
    WRD.HOLIDAY,
    WRD.HOLIDAY_NAME,
    NAA.KWH_IMP AS ACTUAL_CONSUMPTION
