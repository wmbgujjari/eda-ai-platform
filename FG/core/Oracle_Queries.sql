-- name: SEGMENTATION_QUERY
SELECT csrd.CONSUMER_ID,
       bsrd.CAT_CODE,
       bsrd.OFFICE_ID as SECTION_ID,
       bsrd.BILL_UNITS,
       bsrd.CONSUMER_SUPPLY_TYPE,
       bsrd.LOAD_IN_KW,
       ROUND( bsrd.bill_units / NULLIF(bsrd.load_in_kw * (30 * 24), 0), 3 ) AS LOAD_FACTOR,
       ROUND( bsrd.bill_units / NULLIF( ( (bsrd.bill_units / NULLIF(bsrd.load_in_kw * (30 * 24), 0)) * (30 * 24) ), 0 ), 2 ) AS PEAK_DEMAND_KW,
       crd.BILLING_STATUS,
       crd.METER_TYPE,
       crd.METER_PHASE,
       bsrd.ENERGY_CHG,
       bsrd.FIXED_CHG,
       bsrd.ARREAR_TOTAL AS ARREARS,
       bsrd.SUBSIDY,
       bsrd.BILL_AMOUNT,
       csrd.AMOUNT_COLLECTED,
       bsrd.BILL_DUE_DATE,
       csrd.TIME_ID,
       ABS(csrd.TIME_ID - bsrd.BILL_DUE_DATE) AS PAYMENT_LAG_DAYS,
       CASE 
            WHEN csrd.AMOUNT_COLLECTED = 0 THEN 'Bad Debt Risk'
            WHEN (csrd.TIME_ID - bsrd.BILL_DUE_DATE) <= 0 THEN 'On-time'
            WHEN (csrd.TIME_ID - bsrd.BILL_DUE_DATE) BETWEEN 1 AND 30 THEN 'Delayed'
            WHEN (csrd.TIME_ID - bsrd.BILL_DUE_DATE) > 30 THEN 'Default Risk'
            ELSE 'Other'
       END AS PAYMENT_BEHAVIOR,
       ROUND(bsrd.subsidy / NULLIF(bsrd.bill_amount,0), 3) AS SUBSIDY_SHARE,
       ROUND(bsrd.arrear_total / NULLIF(bsrd.bill_amount,0), 3) AS ARREAR_RATIO,
       ROUND(csrd.amount_collected / NULLIF(bsrd.bill_amount,0), 3) AS COLLECTION_RATIO,
       bsrd.DATA_MONTH AS BILL_MONTH,
       bsrd.DATA_year AS BILL_YEAR
FROM mis_user.COLL_STG_RAW_DATA csrd 
JOIN mis_user.BILL_STG_RAW_DATA bsrd 
  ON bsrd.CONSUMER_ID = csrd.CONSUMER_ID
JOIN mis_user.CONSUMER_RAW_DATA crd  
  ON crd.CONSUMER_ID = bsrd.CONSUMER_ID  
WHERE 
    crd.DATA_MONTH='4'
    AND crd.DATA_year='2025' 
    AND bsrd.BILL_DUE_DATE IS NOT NULL
    --AND bsrd.OFFICE_ID IN ({section_ids})    
    AND bsrd.TIME_ID BETWEEN TO_DATE('{start_date}', 'YYYY-MM-DD') AND TO_DATE('{end_date}', 'YYYY-MM-DD')
    AND csrd.TIME_ID BETWEEN TO_DATE('{start_date}', 'YYYY-MM-DD') AND TO_DATE('{end_date}', 'YYYY-MM-DD')

-- name: REVENUE_FORECAST_QUERY_SECTION
SELECT 
    CSRD.OFFICE_ID           AS SECTION_ID, 
    SUM(CSRD.AMOUNT_COLLECTED)  AS COLLECTION,  
    CSRD.COLL_MONTH          AS REV_MONTH, 
    CSRD.COLL_YEAR           AS REV_YEAR
FROM MIS_USER.COLL_STG_RAW_DATA CSRD
WHERE CSRD.DATA_STATUS    = 1
    AND CSRD.CANCL_STATUS   = 'N'
    AND CSRD.IS_CHEQ_BOUNCED = 'N'
    AND CSRD.OFFICE_ID IN ('{section_ids}')
    AND (COLL_YEAR < '{year}' OR (COLL_YEAR = '{year}' AND COLL_MONTH <= '{month}'))
GROUP BY CSRD.OFFICE_ID, CSRD.COLL_MONTH, CSRD.COLL_YEAR
ORDER BY CSRD.OFFICE_ID, CSRD.COLL_YEAR, CSRD.COLL_MONTH