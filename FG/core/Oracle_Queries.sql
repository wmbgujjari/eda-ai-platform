SELECT
    NRD.OFFICE_ID  AS SECTION_ID,NRD.REGISTRATION_NO AS CUSTOMER_ACCOUNT_NO,NRD.PHASE,NRD.CONNECTED_LOAD,NRD.CATEGORY_ID,  
    trunc(nrd.REGISTERED_DATE) AS APPLICATION_DATE,
    TRUNC(NRD.ACTUAL_SERVICE_RELEASE_DATE) AS DAYS,
    TRUNC(NRD.ACTUAL_SERVICE_RELEASE_DATE) - TRUNC(NRD.REGISTERED_DATE) AS DAYS_TAKEN,
    WRD.TEMPERATURE,
    WRD.HUMIDITY,
    WRD.WIND_SPEED,
    WRD.HOLIDAY,
    TO_CHAR(NRD.ACTUAL_SERVICE_RELEASE_DATE, 'D') AS DAY_OF_WEEK,
    CASE 
        WHEN TO_CHAR(NRD.ACTUAL_SERVICE_RELEASE_DATE, 'D') IN ('1', '7') THEN 1
        ELSE 0
    END AS IS_WEEKEND
FROM
MIS_USER.NSC_DATA_ML nrd 
INNER JOIN
    mis_user.WEATHER_RAW_DATA WRD
ON
    TRUNC(NRD.ACTUAL_SERVICE_RELEASE_DATE) = TRUNC(WRD.DAY)
WHERE
    EXTRACT(YEAR FROM ACTUAL_SERVICE_RELEASE_DATE) BETWEEN 2021 AND  2025
	

